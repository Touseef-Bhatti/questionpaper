    <section class="host-guide-section">
        <h2>Online Quiz Hosting for Pakistan Classrooms and Remote Learning</h2>
        <p>
            The live quiz room supports in-person and remote learning in schools, colleges, tuition centres and
            academies across Pakistan. A teacher in Lahore, Karachi, Islamabad, Rawalpindi, Faisalabad, Multan,
            Peshawar, Quetta or another city can prepare a room on a laptop and let students join from phones. The
            workflow is useful for Punjab Board, Federal Board, Sindh, Khyber Pakhtunkhwa, Balochistan and AJK
            learners when the host selects questions matching the correct textbook and syllabus.
        </p>
        <p>
            For a physical classroom, display the room code on the board and allow a minute for everyone to join. For
            an online class, send the direct link before the session and ask students to use a recognizable name. A
            stable connection and modern browser provide the best experience. Keep the host dashboard open on a
            separate tab or screen while participants answer.
        </p>
    </section>

    <section class="host-guide-section host-tips">
        <h2>Tips for a Better Hosted Quiz</h2>
        <ul>
            <li>Test the room with one device before inviting a large group.</li>
            <li>Use clear MCQs with one correct option and four complete answer choices.</li>
            <li>Allow enough time for reading, calculations and slower mobile connections.</li>
            <li>Explain whether students may use books or notes before starting.</li>
            <li>Review difficult questions after the leaderboard instead of focusing only on rank.</li>
            <li>Use short quizzes for lesson checks and longer quizzes for revision or mock tests.</li>
        </ul>
    </section>

    <section class="host-guide-section host-faq">
        <h2>Frequently Asked Questions</h2>
        <details>
            <summary>How do students join my online quiz?</summary>
            <p>Share the generated room code or direct join link. Students enter their name and join the waiting room before the host starts.</p>
        </details>
        <details>
            <summary>Can I host a quiz using only my own questions?</summary>
            <p>Yes. Enable custom questions, add at least one complete MCQ and create the room without selecting a class, book or topic.</p>
        </details>
        <details>
            <summary>Can I mix custom questions with textbook or topic MCQs?</summary>
            <p>Yes. Custom questions can be included alongside questions selected from a class, book, chapters or topics.</p>
        </details>
        <details>
            <summary>What happens after I create the room?</summary>
            <p>You receive a room code, join link and access to the host dashboard where you manage participants and the live session.</p>
        </details>
