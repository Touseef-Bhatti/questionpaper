    <header class="host-guide-header">
        <span class="host-guide-kicker">Live Quiz Hosting Guide</span>
        <h2>How to Host an Online Quiz for Students</h2>
        <p>
            Ahmad Learning Hub gives teachers, tutors, academy instructors and group leaders one place to prepare
            MCQs, create a live room, invite participants and monitor the quiz. You can build a classroom test from a
            selected class and textbook, search questions by topic, generate MCQs from an uploaded file, reuse saved
            questions or type your own. Follow the steps below to create a clear and reliable live quiz session.
        </p>
    </header>

    <section class="host-steps" aria-label="Steps to host a quiz">
        <div class="host-step">
            <span class="host-step-number">1</span>
            <div>
                <h3>Choose where the questions will come from</h3>
                <p>
                    For a textbook quiz, select the class first and then choose its book. To create a
                    subject-independent test, use <strong>Search Questions by Topic</strong>. You can also upload a
                    PDF, Word document, PowerPoint or image and generate MCQs from that file, or switch on
                    <strong>Add Custom Questions</strong> to write or reuse your own MCQs.
                </p>
            </div>
        </div>
        <div class="host-step">
            <span class="host-step-number">2</span>
            <div>
                <h3>Select chapters when needed</h3>
                <p>
                    After selecting a class and book, the chapter list appears automatically. Tick one or more
                    chapters for a focused lesson test. Leave every chapter unselected when you want questions from
                    the complete book. Topic-based and custom-only quizzes do not require a textbook chapter.
                </p>
            </div>
        </div>
        <div class="host-step">
            <span class="host-step-number">3</span>
            <div>
                <h3>Set the question count and quiz duration</h3>
                <p>
                    Enter the total number of MCQs and choose the time allowed. Ten to twenty questions is a practical
                    starting point for a classroom activity. Use a shorter duration for quick recall and a longer
                    duration for conceptual or calculation-based questions. The preview updates before room creation.
                </p>
            </div>
        </div>
        <div class="host-step">
            <span class="host-step-number">4</span>
            <div>
                <h3>Create the room and share access</h3>
                <p>
                    Select <strong>Create Quiz Room</strong> after checking the preview. The next screen provides a
                    room code and direct join link. Share either option through your classroom display, WhatsApp group,
                    learning management system or online meeting chat. Students enter the code and wait in the room.
                </p>
            </div>
        </div>
        <div class="host-step">
            <span class="host-step-number">5</span>
            <div>
                <h3>Open the dashboard and run the live quiz</h3>
                <p>
                    Open the host dashboard to see participants and control the room. Confirm that expected students
                    have joined, then begin the session. During and after the quiz, use live status, scores and the
                    leaderboard to find questions that may need further explanation.
                </p>
            </div>
        </div>
    </section>

